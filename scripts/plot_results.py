def _load_cfg(path: str) -> dict:
    return yaml.safe_load(Path(path).read_text(encoding="utf-8"))["mednext_nnunet"]


def _reports_dir(cfg: dict) -> Path:
    """Directory where export_mednext_results.py writes its CSVs."""
    return Path(cfg["results_folder"]) / "menrt_repo_artifacts" / "reports"


def _default_figures_dir(cfg: dict) -> Path:
    return Path(cfg["results_folder"]) / "menrt_repo_artifacts" / "figures"


# ─────────────────────────────────────────────────────────────────────────────
# CSV readers
# ─────────────────────────────────────────────────────────────────────────────

def _read_manifest(reports_dir: Path) -> list[dict]:
    """
    Load mednext_fold_manifest.csv.
    Only returns rows for folds that have a completed summary.json
    (i.e. training AND nnU-Net validation have finished for that fold).
    Exits with a helpful message if the CSV has not been generated yet.
    """
    csv_path = reports_dir / "mednext_fold_manifest.csv"
    if not csv_path.exists():
        print(
            f"\n[plot_results] ERROR: manifest CSV not found at:\n  {csv_path}\n"
            "Generate it first:\n"
            "  python scripts/export_mednext_results.py --config <your_config.yaml>\n",
            file=sys.stderr,
        )
        sys.exit(1)

    rows: list[dict] = []
    with csv_path.open(encoding="utf-8") as f:
        for row in csv.DictReader(f):
            rows.append(row)

    # Filter to folds with actual metric data
    complete = [r for r in rows if r.get("summary_json_exists", "").lower() == "true"]
    if not complete:
        print(
            "\n[plot_results] ERROR: No completed folds found in the manifest.\n"
            "At least one fold must finish training + nnU-Net validation before plotting.\n",
            file=sys.stderr,
        )
        sys.exit(1)

    print(
        f"[plot_results] Manifest loaded: {len(complete)} completed fold(s) "
        f"(of {len(rows)} configured).",
        flush=True,
    )
    return complete


def _read_per_case(reports_dir: Path) -> list[dict]:
    """
    Load mednext_per_case_metrics.csv.
    Returns an empty list (rather than crashing) if the file is missing,
    because fold-level plots can still be generated without per-case data.
    """
    csv_path = reports_dir / "mednext_per_case_metrics.csv"
    if not csv_path.exists():
        print(
            f"[plot_results] WARNING: per-case CSV not found at {csv_path}. "
            "Case-level plots will be skipped.",
            file=sys.stderr,
        )
        return []

    rows: list[dict] = []
    with csv_path.open(encoding="utf-8") as f:
        for row in csv.DictReader(f):
            rows.append(row)

    print(f"[plot_results] Per-case metrics loaded: {len(rows)} rows.", flush=True)
    return rows


