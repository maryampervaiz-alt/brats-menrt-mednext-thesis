def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=(
            "Generate publication-ready figures from MedNeXt MEN-RT training results. "
            "Must be run after export_mednext_results.py."
        )
    )
    p.add_argument(
        "--config",
        type=str,
        default="configs/mednext_nnunet.yaml",
        help="Path to mednext_nnunet.yaml.",
    )
    p.add_argument(
        "--out-dir",
        type=str,
        default="",
        help=(
            "Output directory for figures. "
            "Default: <results_folder>/menrt_repo_artifacts/figures/"
        ),
    )
    p.add_argument(
        "--dpi",
        type=int,
        default=300,
        help="Resolution for saved PNG figures (300 = publication quality).",
    )
    p.add_argument(
        "--top-n",
        type=int,
        default=5,
        help="Number of best / worst cases to report in best_worst_cases.csv.",
    )
    return p.parse_args()


