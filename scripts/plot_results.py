# Colourblind-safe palette (Wong 2011, widely used in scientific publications)
_FOLD_COLOURS = ["#0072B2", "#E69F00", "#009E73", "#D55E00", "#CC79A7"]

# Consistent colour assignment for each metric type
_METRIC_COLOURS = {
    "Dice":      "#0072B2",
    "Jaccard":   "#009E73",
    "HD95":      "#E69F00",
    "Precision": "#D55E00",
    "Recall":    "#CC79A7",
}


def _to_float(value: object) -> float | None:
    """Safely convert any CSV value to float; returns None for missing/NaN."""
    try:
        v = float(str(value).strip())
        return v if np.isfinite(v) else None
    except (ValueError, TypeError):
        return None


def _apply_publication_style() -> None:
    """
    Apply a clean, minimal matplotlib style suitable for thesis/MICCAI figures.
    Removes chartjunk (top/right spines), uses readable font sizes, light grid.
    """
    plt.rcParams.update(
        {
            "font.family":        "DejaVu Sans",
            "font.size":          11,
            "axes.titlesize":     13,
            "axes.titleweight":   "bold",
            "axes.labelsize":     12,
            "xtick.labelsize":    10,
            "ytick.labelsize":    10,
            "legend.fontsize":    10,
            "legend.framealpha":  0.85,
            "axes.spines.top":    False,
            "axes.spines.right":  False,
            "axes.grid":          True,
            "grid.alpha":         0.35,
            "grid.linestyle":     "--",
            "grid.linewidth":     0.6,
        }
    )


# ─────────────────────────────────────────────────────────────────────────────
# Figure 1 — Grouped bar chart: mean metrics per fold
# ─────────────────────────────────────────────────────────────────────────────

def plot_fold_metrics_bar(manifest: list[dict], out_path: Path, dpi: int) -> None:
    """
    Grouped bar chart with one group per completed fold.
    Primary y-axis: dimensionless metrics (Dice, Jaccard, Precision, Recall).
    Secondary y-axis: HD95 in mm (different scale → twin axis with line plot).

    Why both axes? Combining mm-scale and 0-1 scale on a single axis is
    misleading and a common criticism at thesis defences.
    """
    fold_ids = [int(r["fold"]) for r in manifest]

    primary_keys   = ["dice_mean",  "jaccard_mean", "precision_mean", "recall_mean"]
    primary_labels = ["Dice",       "Jaccard",      "Precision",      "Recall"]

    hd95_vals = [_to_float(r.get("hd95_mean")) for r in manifest]
    has_hd95  = any(v is not None for v in hd95_vals)

    fig, ax1 = plt.subplots(figsize=(max(8, 1.8 * len(fold_ids) + 2), 5.5))

    n_groups      = len(fold_ids)
    n_primary     = len(primary_keys)
    bar_width     = 0.14
    group_spacing = 0.75
    group_centers = np.arange(n_groups) * group_spacing

    for b_idx, (col_key, label) in enumerate(zip(primary_keys, primary_labels)):
        vals    = [_to_float(r.get(col_key)) for r in manifest]
        offsets = group_centers + (b_idx - n_primary / 2.0 + 0.5) * bar_width
        bars    = ax1.bar(
            offsets,
            [v if v is not None else 0.0 for v in vals],
            width=bar_width,
            color=_METRIC_COLOURS[label],
            label=label,
            edgecolor="white",
            linewidth=0.6,
            zorder=3,
        )
        # Value annotation on top of each bar (rotated to save space)
        for bar, v in zip(bars, vals):
            if v is not None:
                ax1.text(
                    bar.get_x() + bar.get_width() / 2.0,
                    bar.get_height() + 0.008,
                    f"{v:.3f}",
                    ha="center",
                    va="bottom",
                    fontsize=7,
                    rotation=90,
                )

    ax1.set_xlabel("Fold")
    ax1.set_ylabel("Score (0 – 1)  ↑ higher is better")
    ax1.set_ylim(0.0, 1.20)
    ax1.set_xticks(group_centers)
    ax1.set_xticklabels([f"Fold {f}" for f in fold_ids])
    ax1.legend(loc="upper left", ncol=2)

    # HD95 overlaid as a line on the secondary y-axis
    if has_hd95:
        ax2 = ax1.twinx()
        ax2.plot(
            group_centers,
            [v if v is not None else np.nan for v in hd95_vals],
            color=_METRIC_COLOURS["HD95"],
            marker="D",
            linewidth=2.0,
            markersize=8,
            label="HD95 (mm)",
            zorder=5,
        )
        # Annotate HD95 values
        for x, v in zip(group_centers, hd95_vals):
            if v is not None:
                ax2.text(x, v + 0.4, f"{v:.1f}", ha="center", va="bottom",
                         fontsize=8, color=_METRIC_COLOURS["HD95"])
        ax2.set_ylabel("HD95 (mm)  ↓ lower is better", color=_METRIC_COLOURS["HD95"])
        ax2.tick_params(axis="y", labelcolor=_METRIC_COLOURS["HD95"])
        ax2.spines["right"].set_visible(True)
        ax2.spines["top"].set_visible(False)
        ax2.legend(loc="upper right")

    ax1.set_title(
        "MedNeXt-S  |  BraTS MEN-RT Task502  |  Per-Fold Segmentation Metrics\n"
        "GTV Segmentation  —  T1c → GTV  (meningioma radiotherapy planning)",
        pad=12,
    )
    fig.tight_layout()
    fig.savefig(out_path, dpi=dpi, bbox_inches="tight")
    plt.close(fig)
    print(f"[plot_results] Saved → {out_path}", flush=True)


# ─────────────────────────────────────────────────────────────────────────────
# Figures 2 & 3 — Per-case distribution: box plots with jitter overlay
# ─────────────────────────────────────────────────────────────────────────────

def _per_case_boxplot(
    per_case: list[dict],
    metric_key: str,
    metric_label: str,
    y_label: str,
    out_path: Path,
    dpi: int,
    y_min: float = 0.0,
    y_max: float | None = None,
) -> None:
    """
    Box plot (IQR + whiskers) with individual data points overlaid as a
    jitter plot.  Combining both avoids hiding distributional details while
    still giving a summary of spread — the recommended approach for small-to-
    medium sample sizes in clinical AI papers.
    """
    fold_groups: dict[int, list[float]] = {}
    for row in per_case:
        v = _to_float(row.get(metric_key))
        if v is not None:
            fold_groups.setdefault(int(row["fold"]), []).append(v)

    if not fold_groups:
        print(
            f"[plot_results] WARNING: No valid values for '{metric_key}'. "
            "Skipping box plot.",
            file=sys.stderr,
        )
        return

    sorted_folds = sorted(fold_groups.keys())
    data         = [fold_groups[f] for f in sorted_folds]
    tick_labels  = [f"Fold {f}\n(n={len(fold_groups[f])})" for f in sorted_folds]
    colours      = [_FOLD_COLOURS[i % len(_FOLD_COLOURS)] for i in range(len(sorted_folds))]

    fig, ax = plt.subplots(figsize=(max(6, 1.8 * len(sorted_folds) + 2), 5.5))

    bp = ax.boxplot(
        data,
        patch_artist=True,
        notch=False,
        showfliers=False,   # outliers shown via jitter instead
        medianprops={"color": "black", "linewidth": 2.5},
        whiskerprops={"linewidth": 1.5},
        capprops={"linewidth": 1.5},
        boxprops={"linewidth": 1.5},
        widths=0.4,
    )

    for patch, colour in zip(bp["boxes"], colours):
        patch.set_facecolor(colour)
        patch.set_alpha(0.45)

    # Jitter overlay: each point represents one validation case
    for i, (fold_data, colour) in enumerate(zip(data, colours), start=1):
        rng      = np.random.default_rng(seed=i + 100)
        x_jitter = rng.uniform(-0.20, 0.20, size=len(fold_data))
        ax.scatter(
            [i + j for j in x_jitter],
            fold_data,
            color=colour,
            s=22,
            alpha=0.70,
            zorder=4,
            edgecolors="white",
            linewidths=0.5,
        )

    # Annotate median above each box
    for i, fold_data in enumerate(data, start=1):
        med = float(np.median(fold_data))
        offset = 0.008 if (y_max is None or y_max <= 1.5) else 0.5
        ax.text(i, med + offset, f"{med:.3f}",
                ha="center", va="bottom", fontsize=8, fontweight="bold")

    ax.set_xticklabels(tick_labels)
    ax.set_xlabel("Fold")
    ax.set_ylabel(y_label)
    ax.set_ylim(bottom=y_min, top=y_max)
    ax.set_title(
        f"MedNeXt-S  |  BraTS MEN-RT  |  Per-Case {metric_label} Distribution\n"
        "GTV Segmentation  —  T1c → GTV  (meningioma radiotherapy planning)",
        pad=12,
    )

    fig.tight_layout()
    fig.savefig(out_path, dpi=dpi, bbox_inches="tight")
    plt.close(fig)
    print(f"[plot_results] Saved → {out_path}", flush=True)


# ─────────────────────────────────────────────────────────────────────────────
# Figure 4 — Dice vs HD95 scatter (diagnostic plot)
# ─────────────────────────────────────────────────────────────────────────────

def plot_dice_vs_hd95(per_case: list[dict], out_path: Path, dpi: int) -> None:
    """
    Scatter plot of Dice vs HD95 per case, coloured by fold.

    This plot is diagnostically important: cases in the top-right quadrant
    (high Dice, high HD95) indicate volumetrically-good but boundary-poor
    predictions — common in meningioma where the model gets the bulk correct
    but misses small protrusions.  Reviewers often ask about this specifically.
    """
    fold_groups: dict[int, tuple[list[float], list[float]]] = {}
    for row in per_case:
        dice = _to_float(row.get("dice"))
        hd95 = _to_float(row.get("hd95"))
        if dice is not None and hd95 is not None:
            fold = int(row["fold"])
            if fold not in fold_groups:
                fold_groups[fold] = ([], [])
            fold_groups[fold][0].append(dice)
            fold_groups[fold][1].append(hd95)

    if not fold_groups:
        print("[plot_results] WARNING: No Dice+HD95 pairs found. Skipping scatter.", file=sys.stderr)
        return

    fig, ax = plt.subplots(figsize=(7, 5.5))

    for i, (fold, (d_vals, h_vals)) in enumerate(sorted(fold_groups.items())):
        ax.scatter(
            d_vals, h_vals,
            color=_FOLD_COLOURS[i % len(_FOLD_COLOURS)],
            label=f"Fold {fold}",
            alpha=0.72,
            s=35,
            edgecolors="white",
            linewidths=0.5,
            zorder=3,
        )

    ax.set_xlabel("Dice Similarity Coefficient  (↑ higher is better)")
    ax.set_ylabel("Hausdorff Distance 95  in mm  (↓ lower is better)")
    ax.set_title(
        "MedNeXt-S  |  BraTS MEN-RT  |  Dice vs HD95 per Case\n"
        "GTV Segmentation  —  T1c → GTV  (meningioma radiotherapy planning)",
        pad=12,
    )
    ax.legend(title="Fold", framealpha=0.9)

    # Annotate quadrants to aid interpretation in thesis text
    xlim = ax.get_xlim()
    ylim = ax.get_ylim()
    ax.text(
        xlim[0] + 0.01 * (xlim[1] - xlim[0]),
        ylim[1] - 0.03 * (ylim[1] - ylim[0]),
        "Low Dice\nHigh HD95\n(poor)",
        fontsize=7, color="gray", va="top",
    )
    ax.text(
        xlim[1] - 0.01 * (xlim[1] - xlim[0]),
        ylim[0] + 0.03 * (ylim[1] - ylim[0]),
        "High Dice\nLow HD95\n(ideal)",
        fontsize=7, color="gray", va="bottom", ha="right",
    )

    fig.tight_layout()
    fig.savefig(out_path, dpi=dpi, bbox_inches="tight")
    plt.close(fig)
    print(f"[plot_results] Saved → {out_path}", flush=True)


# ─────────────────────────────────────────────────────────────────────────────
# Table — Mean ± SD per fold  (LaTeX-ready CSV)
# ─────────────────────────────────────────────────────────────────────────────

def write_fold_metrics_table(
    manifest: list[dict],
    per_case: list[dict],
    out_path: Path,
) -> None:
    """
    Writes a CSV where each row is one fold (plus an 'Overall' summary row).
    Each metric column reports  mean ± SD  computed from per-case values.

    The format maps directly to a LaTeX tabular table: paste the values
    in as  $0.xxxx \\pm 0.xxxx$ .
    """
    # Collect per-case values indexed by fold
    case_vals: dict[int, dict[str, list[float]]] = {}
    for row in per_case:
        fold = int(row["fold"])
        case_vals.setdefault(fold, {})
        for key in ("dice", "jaccard", "hd95", "precision", "recall"):
            v = _to_float(row.get(key))
            if v is not None:
                case_vals[fold].setdefault(key, []).append(v)

    fieldnames = [
        "fold", "n_cases",
        "dice_mean", "dice_sd",
        "jaccard_mean", "jaccard_sd",
        "hd95_mean", "hd95_sd",
        "precision_mean", "precision_sd",
        "recall_mean", "recall_sd",
    ]

    def _stats(vals: list[float]) -> tuple[str, str]:
        """Return (mean_str, sd_str) or ('', '') if empty."""
        if not vals:
            return ("", "")
        m = float(np.mean(vals))
        s = float(np.std(vals, ddof=1)) if len(vals) > 1 else 0.0
        return f"{m:.4f}", f"{s:.4f}"

    rows_out: list[dict] = []
    for r in manifest:
        fold   = int(r["fold"])
        fv     = case_vals.get(fold, {})
        n      = len(fv.get("dice", []))
        d_m,  d_s  = _stats(fv.get("dice",      []))
        j_m,  j_s  = _stats(fv.get("jaccard",   []))
        h_m,  h_s  = _stats(fv.get("hd95",      []))
        pr_m, pr_s = _stats(fv.get("precision", []))
        re_m, re_s = _stats(fv.get("recall",    []))
        rows_out.append({
            "fold": fold, "n_cases": n,
            "dice_mean": d_m,  "dice_sd":  d_s,
            "jaccard_mean": j_m,  "jaccard_sd":  j_s,
            "hd95_mean": h_m,  "hd95_sd":  h_s,
            "precision_mean": pr_m, "precision_sd": pr_s,
            "recall_mean":    re_m, "recall_sd":    re_s,
        })

    # Overall row: pool all per-case values across folds
    all_vals: dict[str, list[float]] = {}
    for fv in case_vals.values():
        for key, vals in fv.items():
            all_vals.setdefault(key, []).extend(vals)

    total_cases = sum(len(fv.get("dice", [])) for fv in case_vals.values())
    d_m,  d_s  = _stats(all_vals.get("dice",      []))
    j_m,  j_s  = _stats(all_vals.get("jaccard",   []))
    h_m,  h_s  = _stats(all_vals.get("hd95",      []))
    pr_m, pr_s = _stats(all_vals.get("precision", []))
    re_m, re_s = _stats(all_vals.get("recall",    []))
    rows_out.append({
        "fold": "Overall", "n_cases": total_cases,
        "dice_mean": d_m,  "dice_sd":  d_s,
        "jaccard_mean": j_m,  "jaccard_sd":  j_s,
        "hd95_mean": h_m,  "hd95_sd":  h_s,
        "precision_mean": pr_m, "precision_sd": pr_s,
        "recall_mean":    re_m, "recall_sd":    re_s,
    })

    with out_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows_out)

    print(f"[plot_results] Saved → {out_path}", flush=True)


# ─────────────────────────────────────────────────────────────────────────────
# Table — Best / Worst cases by Dice
# ─────────────────────────────────────────────────────────────────────────────

def write_best_worst_cases(
    per_case: list[dict], out_path: Path, top_n: int = 5
) -> None:
    """
    Identify the top-N and bottom-N cases by Dice score across all folds.

    These case IDs are later fed to visualize_predictions.py to generate
    qualitative segmentation overlay figures for the thesis.
    Having explicit best/worst case analysis is expected at MICCAI level.
    """
    valid = [r for r in per_case if _to_float(r.get("dice")) is not None]
    if not valid:
        print(
            "[plot_results] WARNING: No valid per-case Dice values. "
            "Skipping best/worst export.",
            file=sys.stderr,
        )
        return

    by_dice = sorted(valid, key=lambda r: _to_float(r["dice"]) or 0.0)
    worst   = by_dice[:top_n]
    best    = list(reversed(by_dice[-top_n:]))

    fieldnames = ["rank", "category", "fold", "case_id",
                  "dice", "jaccard", "hd95", "precision", "recall"]
    rows_out: list[dict] = []
    for rank, row in enumerate(best, start=1):
        rows_out.append({"rank": rank, "category": "best",
                         **{k: row.get(k, "") for k in fieldnames[2:]}})
    for rank, row in enumerate(worst, start=1):
        rows_out.append({"rank": rank, "category": "worst",
                         **{k: row.get(k, "") for k in fieldnames[2:]}})

    with out_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows_out)

    print(f"[plot_results] Saved → {out_path}", flush=True)

    print(f"\n[plot_results] Top-{top_n} BEST cases  (Dice ↑):", flush=True)
    for r in best:
        print(f"  fold={r['fold']}  {r['case_id']}  "
              f"Dice={r.get('dice','?')}  HD95={r.get('hd95','?')} mm", flush=True)
    print(f"[plot_results] Top-{top_n} WORST cases (Dice ↓):", flush=True)
    for r in worst:
        print(f"  fold={r['fold']}  {r['case_id']}  "
              f"Dice={r.get('dice','?')}  HD95={r.get('hd95','?')} mm", flush=True)


# ─────────────────────────────────────────────────────────────────────────────
# Main
# ─────────────────────────────────────────────────────────────────────────────

def main() -> None:
    args = parse_args()
    _apply_publication_style()

    cfg         = _load_cfg(args.config)
    reports_dir = _reports_dir(cfg)
    out_dir     = Path(args.out_dir) if args.out_dir else _default_figures_dir(cfg)
    out_dir.mkdir(parents=True, exist_ok=True)

    print(f"[plot_results] Config     : {args.config}", flush=True)
    print(f"[plot_results] Reports dir: {reports_dir}", flush=True)
    print(f"[plot_results] Figures dir: {out_dir}", flush=True)

    # ── Load data ──────────────────────────────────────────────────────────
    manifest = _read_manifest(reports_dir)   # exits if not available
    per_case = _read_per_case(reports_dir)   # empty list if not available

    # ── Figure 1: grouped bar chart, one group per fold ────────────────────
    plot_fold_metrics_bar(
        manifest,
        out_path=out_dir / "fold_metrics_bar.png",
        dpi=args.dpi,
    )

    if per_case:
        # ── Figure 2: per-case Dice box + jitter ───────────────────────────
        _per_case_boxplot(
            per_case,
            metric_key="dice",
            metric_label="Dice (DSC)",
            y_label="Dice Similarity Coefficient  (↑ higher is better)",
            out_path=out_dir / "per_case_dice_boxplot.png",
            dpi=args.dpi,
            y_min=0.0,
            y_max=1.05,
        )

        # ── Figure 3: per-case HD95 box + jitter ───────────────────────────
        _per_case_boxplot(
            per_case,
            metric_key="hd95",
            metric_label="HD95 (mm)",
            y_label="Hausdorff Distance 95th percentile  in mm  (↓ lower is better)",
            out_path=out_dir / "per_case_hd95_boxplot.png",
            dpi=args.dpi,
            y_min=0.0,
        )

        # ── Figure 4: Dice vs HD95 scatter (diagnostic) ────────────────────
        plot_dice_vs_hd95(
            per_case,
            out_path=out_dir / "dice_vs_hd95_scatter.png",
            dpi=args.dpi,
        )

        # ── Table: fold-level mean ± SD (LaTeX-ready) ──────────────────────
        write_fold_metrics_table(
            manifest,
            per_case,
            out_path=out_dir / "fold_metrics_table.csv",
        )

        # ── Table: best / worst cases for qualitative review ───────────────
        write_best_worst_cases(
            per_case,
            out_path=out_dir / "best_worst_cases.csv",
            top_n=args.top_n,
        )

    print(f"\n[plot_results] All outputs saved to: {out_dir}", flush=True)
    print("[plot_results] Done.", flush=True)


