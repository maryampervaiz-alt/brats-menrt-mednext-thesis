"""
scripts/plot_results.py
=======================
Post-training visualization for the MedNeXt MEN-RT pipeline.

Reads the CSV reports generated by  export_mednext_results.py  and produces
publication-ready figures suitable for a MICCAI paper or thesis.

This script is intentionally read-only: it never modifies training artifacts.
It exits with a clear error message if the required CSVs do not yet exist,
so it is safe to call even before training completes (partial results are
handled gracefully — only completed folds are plotted).

Evaluation metrics used (all standard for BraTS / RT-planning tasks):
  - Dice Similarity Coefficient (DSC)   : volumetric overlap, primary metric
  - Hausdorff Distance 95th pct (HD95)  : shape / boundary quality (in mm)
  - Jaccard Index (IoU)                 : alternative overlap measure
  - Precision                           : false-positive sensitivity
  - Recall (Sensitivity)                : false-negative sensitivity

Outputs saved to  <results_folder>/menrt_repo_artifacts/figures/  (configurable):
  fold_metrics_bar.png        Mean DSC / HD95 / Precision / Recall per fold
  fold_metrics_table.csv      Mean ± SD per fold  (copy-paste into LaTeX)
  per_case_dice_boxplot.png   Per-case Dice distribution across folds
  per_case_hd95_boxplot.png   Per-case HD95 distribution across folds
  dice_vs_hd95_scatter.png    Scatter: Dice vs HD95, coloured by fold
  best_worst_cases.csv        Top-5 and bottom-5 cases ranked by Dice

Usage
-----
  python scripts/plot_results.py --config configs/mednext_nnunet.yaml
  python scripts/plot_results.py --config configs/mednext_nnunet.yaml \\
      --out-dir /kaggle/working/figures --dpi 300
"""
from __future__ import annotations

import argparse
import csv
import sys
from pathlib import Path

import matplotlib
# Use non-interactive backend so the script works on headless Kaggle/cluster
# environments that have no display server (X11).
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
import yaml


# ─────────────────────────────────────────────────────────────────────────────
# CLI
